package dhomo.crmmail.api.message;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonProperty;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import java.util.Objects;

@Getter
@Setter
@NoArgsConstructor
public class Recipient {

    private String type;
    private String address;

    @JsonCreator
    public Recipient(@JsonProperty("type") String type, @JsonProperty("address") String address) {
        this.type = type;
        this.address = address;
    }


    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Recipient recipient = (Recipient) o;
        return Objects.equals(type, recipient.type) &&
                Objects.equals(address, recipient.address);
    }

    @Override
    public int hashCode() {
        return Objects.hash(type, address);
    }
}
